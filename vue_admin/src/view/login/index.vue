<template>
  <div class="login-container">
    <el-form
      ref="loginForm"
      :model="loginData"
      class="login-form"
      auto-complete="on"
      label-position="left"
    >
      <h3 class="title">后台管理系统</h3>
      <el-form-item prop="username">
        <span class="svg-container svg-container_login">
          <svg-icon icon-class="user"/>
        </span>
        <el-input
          v-model="loginData.username"
          ref="userNameInput"
          name="username"
          type="text"
          auto-complete="on"
          placeholder="username"
        />
      </el-form-item>
      <el-form-item prop="password">
        <span class="svg-container">
          <svg-icon icon-class="password"/>
        </span>
        <el-input
          :type="pwdType"
          v-model="loginData.password"
          name="password"
          auto-complete="on"
          placeholder="password"
        />
        <span class="show-pwd" @click="showPwd">
          <svg-icon icon-class="eye"/>
        </span>
      </el-form-item>
     <el-form-item>
        <el-button
          :loading="loading"
          type="primary"
          style="width:100%;"
          @click.native.prevent="handleLogin"
        >登 录</el-button>
      </el-form-item>
    </el-form>
 


  </div>
</template>

<script>

export default {
  name: "Login",
  data() {
    return {
      /* labelPosition:'right', */
      loginData:{
               username:'',
               password:''
      },
      webSiteName: "",
      loading: false,
      pwdType: "password",
      redirect: undefined,
    };
  },

  mounted() {
    // mounted钩子函数，dom已经渲染完毕，可以直接获取到dom对象进行聚焦
    this.$refs.userNameInput.focus()
  },
  created() {
    // created，dom还未开始渲染，因此需要使用this.$nextTick 将其放置在下一个dom渲染操作时执行
    // this.$refs.userNameInput.focus()
    this.getWebName()
  },
  methods: {
    handleLogin() {
             this.$refs['loginForm'].validate((valid) => {
                if (valid) {
                  this.loading = true
                  this.$store.dispatch('user/Login', this.loginData).then(() => {
                  this.$router.push({ path: this.redirect || '/dashboard' })
                  this.loading = false
                 }).catch(() => {
                    this.loading = false
                  })
                  }
                else{
                   console.log('error submit!!')
                   return false
                }
              });
     },
    showPwd() {
      if (this.pwdType === "password") {
        this.pwdType = "";
      } else {
        this.pwdType = "password";
      }
    }
  }
};
</script>

